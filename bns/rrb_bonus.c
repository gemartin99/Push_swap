#include "../inc/pushswap_bonus.h"

void	rrb(t_list *d)
{
	int	temp;
	int	i;

	temp = d->sb[d->sib - 1];
	i = d->sib - 1;
	while (--i >= 0)
		d->sb[i + 1] = d->sb[i];
	d->sb[0] = temp;
}
