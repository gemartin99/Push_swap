#include "pushswap.h"

int	ft_space(char *s)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (s[i])
	{
		if (s[i] == ' ' && (s[i + 1] != ' ' || s[i + 1] != '\0'))
			j++;
		i++;
	}
	return (j);
}
